dotenv.config({ path: './config.env' });
const app = require('./app');

const DB = "mongodb+srv://SleepyAnand:blabla@cluster0.qx1pd.mongodb.net/transaction"

//mongoose.connect("mongodb+srv://SleepyAnand:blabla@cluster0.qx1pd.mongodb.net/transaction", {useNewUrlParser: true, useUnifiedTopology: true});

